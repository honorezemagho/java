import java.util.Arrays;

public class Main {
    public static void main(String[] args) {
        // Implicit casting or Automatic Casting
        short x = 1;
        int y = x + 2;
        System.out.println(y);

        double k = 1.1;
        double z = k + 2;
        System.out.println(z);

        // Explicit Casting
        double field1 = 1.1;
        int field2 = (int) x + 2;
        System.out.println(field2);
    }
}

